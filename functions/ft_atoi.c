#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		sign;
	size_t	convert;

	i = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i++] == '-')
			sign = -1;
	}
	while (str[i] == '0')
		i++;
	convert = 0;
	while (str[i] >= 48 && str[i] <= 57)
		convert = convert * 10 + str[i++] - 48;
	return (convert * sign);
}
